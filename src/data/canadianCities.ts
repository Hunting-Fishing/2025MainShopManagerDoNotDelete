// Get cities grouped by province
export function getCitiesByProvince(): Record<string, CanadianCity[]> {
  const grouped: Record<string, CanadianCity[]> = {};
  
  for (const city of CANADIAN_CITIES) {
    if (!grouped[city.province]) {
      grouped[city.province] = [];
    }
    grouped[city.province].push(city);
  }
  
  // Sort cities within each province
  for (const province in grouped) {
    grouped[province].sort((a, b) => a.city.localeCompare(b.city));
  }
  
  return grouped;
}

// Search cities by query
export function searchCities(query: string): CanadianCity[] {
  if (!query || query.length < 2) return [];
  
  const lowerQuery = query.toLowerCase();
  
  return CANADIAN_CITIES.filter(c => 
    c.city.toLowerCase().includes(lowerQuery) ||
    c.province.toLowerCase().includes(lowerQuery) ||
    PROVINCE_NAMES[c.province]?.toLowerCase().includes(lowerQuery)
  ).slice(0, 50); // Limit results for performance
}
